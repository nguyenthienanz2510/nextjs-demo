import Head from "next/head";
import Image from "next/image";
import Link from "next/link";
import { useEffect } from "react";
import MainLayout from "../components/Layout/MainLayout";
import SpinnerComponent from "../components/Loading";
import { BASE_URL, postService } from "../services/post";
import * as React from "react";
import Card from "@mui/material/Card";
import CardActions from "@mui/material/CardActions";
import CardContent from "@mui/material/CardContent";
import CardMedia from "@mui/material/CardMedia";
import Button from "@mui/material/Button";
import Typography from "@mui/material/Typography";

export default function Home({ posts }) {
  console.log(posts);
  return (
    <>
      <Head>
        <title>Mini NextJs</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {!posts && <SpinnerComponent />}
      <MainLayout>
        <div className="my-12 container mx-auto">
          <div className="grid grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-4">
            {!!posts &&
              posts.map((post) => {
                return (
                  <>
                    <Card sx={{ maxWidth: 345 }} key={post._id} className="transition-all hover:bg-gray-200">
                      <CardMedia
                        component="img"
                        alt={post.title}
                        style={{ objectFit: "cover", height: "150px" }}
                        image={`${BASE_URL}/${post.imageUrl}`}
                      />
                      <CardContent>
                        <Typography gutterBottom variant="h5" component="div" className="truncate">
                          {post.title}
                        </Typography>
                        <Typography variant="body2" color="text.secondary" className="text-truncation-2">
                          {post.content}
                        </Typography>
                      </CardContent>
                      <CardActions className="px-4 pb-4 pt-0">
                        <Link key={post._id} href={`/post/${post._id}`}>
                          <a className="text-color-primary">
                            <Button size="small" >Read More</Button>
                          </a>
                        </Link>
                      </CardActions>
                    </Card>
                  </>
                );
              })}
          </div>
        </div>
      </MainLayout>
    </>
  );
}

export async function getServerSideProps() {
  try {
    const res = await postService.getPosts();
    const posts = res.data.posts;
    return {
      props: { posts },
    };
  } catch (error) {
    console.log(error);
    return {
      props: {},
    };
  }
}
